# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# Helper: run schedule_day() at 11:00 ðŸ‡®ðŸ‡¹ every day
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
async def daily_scheduler_loop():
    """
    Background task: sleeps until the next 11:00 Europe/Rome, then
    executes schedule_day(bot).  Loops forever.
    """
    while True:
        now = datetime.now(italy_tz)

        target = datetime.combine(
            now.date(), dt_time(hour=11, minute=0, tzinfo=italy_tz)
        )
        if now >= target:                # itâ€™s already after 11:00 â†’ next day
            target += timedelta(days=1)

        delta = target - now
        log_info(
            f"ðŸ•š Daily loop sleeping {delta.seconds//3600}h{(delta.seconds//60)%60}m "
            f"until {target.strftime('%d %b %H:%M')}"
        )
        await asyncio.sleep(delta.total_seconds())

        # 11:00 reached
        log_info("ðŸ•š Running daily schedule_day()")
        try:
            await schedule_day(bot)
        except Exception as exc:         # donâ€™t let the helper die silently
            log_error(f"Exception in daily schedule_day â€¢ {exc}")

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# Helper: greet & print todayâ€™s fixtures (via schedule_day) once on boot
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
async def first_boot_sequence():
    # power-save tweaks
    setup_power_management()

    # greeting in Discord
    channel = bot.get_channel(CHANNEL_ID)
    if channel:
        await channel.send(get_greeting())

    # run morning logic once immediately
    await schedule_day(bot)


# â”€â”€â”€ Discord events â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
@bot.event
async def on_ready():
    log_info(f"âœ… Logged in as {bot.user}")

    # start background loop only once
    if not getattr(bot, "_daily_task_started", False):
        bot.loop.create_task(daily_scheduler_loop())
        bot._daily_task_started = True

    # run first-boot logic
    await first_boot_sequence()


# â”€â”€â”€ Cog loader (optional) â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
def load_all_cogs():
    cogs_dir = os.path.join(os.path.dirname(__file__), "cogs")
    if not os.path.isdir(cogs_dir):
        return
    for fname in os.listdir(cogs_dir):
        if fname.endswith(".py") and fname != "__init__.py":
            try:
                bot.load_extension(f"cogs.{fname[:-3]}")
                log_info(f"âœ” loaded cog: cogs.{fname[:-3]}")
            except Exception as exc:
                log_error(f"Failed loading cog {fname}: {exc}")


# â”€â”€â”€ main â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
